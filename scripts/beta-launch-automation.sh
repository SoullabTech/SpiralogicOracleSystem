# Notification function with Slack/Telegram support
notify() {
    local phase="$1"
    local status="$2"
    local message="$3"
    local color="$4"
    
    local emoji
    case $status in
        "START") emoji="ðŸš€" ;;
        "SUCCESS") emoji="âœ…" ;;
        "WARNING") emoji="âš ï¸" ;;
        "ERROR") emoji="âŒ" ;;
        "INFO") emoji="ðŸ“‹" ;;
        *) emoji="ðŸ””" ;;
    esac
    
    local full_message="$emoji **SPIRALOGIC BETA LAUNCH**
